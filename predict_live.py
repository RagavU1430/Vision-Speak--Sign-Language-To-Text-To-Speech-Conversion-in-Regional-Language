def transliterate_tamil_text(text: str) -> str:
    """
    Transliterates Tamil Unicode text into Romanized phonetic English fallback
    so that default English TTS voices can read them audibly.
    """
    if not text:
        return text
        
    cleaned = text.strip()
    # Check direct match
    if cleaned in TAMIL_PHONETIC_DICT:
        return TAMIL_PHONETIC_DICT[cleaned]
        
    # Split text into words/phrases and check
    words = cleaned.split()
    romanized_words = []
    for word in words:
        stripped = word.strip(".,!?\"'()[]{}")
        if stripped in TAMIL_PHONETIC_DICT:
            romanized_words.append(TAMIL_PHONETIC_DICT[stripped])
        else:
            # Find if this Tamil word corresponds to any English word in our translation dict
            found_eng = None
            for eng_k, tamil_v in TRANSLATION_DICT.get("Tamil", {}).items():
                if tamil_v == stripped:
                    found_eng = eng_k
                    break
            if found_eng:
                # Use phonetic version of the English word or just the English word itself
                romanized_words.append(found_eng.lower())
            else:
                pass
                
    if romanized_words:
        return " ".join(romanized_words)
    return ""


# ── Global Translator & Cache for Real-Time Translation ──────────────────────
_google_translator = None
_translation_cache = {}

def translate_to_tamil(text: str) -> str:
    """
    Translates English text to Tamil using googletrans==4.0.0rc1.
    Utilizes local dictionary mapping first, then memory cache, and finally 
    falls back to the googletrans API to prevent UI freezing and API limit issues.
    """
    global _google_translator
    if not text or not text.strip():
        return text

    cleaned_text = text.strip()
    upper_text = cleaned_text.upper()

    # 1. Try local dictionary lookup first (instant & offline-friendly)
    lang_dict = TRANSLATION_DICT.get("Tamil", {})
    if upper_text in lang_dict:
        return lang_dict[upper_text]

    # 2. Check memory cache for previous translations
    if cleaned_text in _translation_cache:
        return _translation_cache[cleaned_text]

    # 3. Call googletrans API
    try:
        if _google_translator is None:
            from googletrans import Translator
            _google_translator = Translator()
        
        result = _google_translator.translate(cleaned_text, src='en', dest='ta')
        if result and result.text:
            _translation_cache[cleaned_text] = result.text
            return result.text
        else:
            print("[ERROR] Tamil Translation Failed")
    except Exception as e:
        print(f"[ERROR] Tamil Translation Failed: {e}")

    # Cache original text on failure to prevent spamming the API on every frame
    _translation_cache[cleaned_text] = text
    return text


