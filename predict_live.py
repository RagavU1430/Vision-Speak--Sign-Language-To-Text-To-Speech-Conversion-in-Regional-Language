# Translation Engine (modular design for multilingual support)
# ═══════════════════════════════════════════════════════════════════════════

class TranslationEngine:
    """
    Modular translation engine supporting English → target language.

    Currently uses googletrans-based translation for Tamil with local dictionary backup.
    Designed to seamlessly integrate API-based translation (Google Translate,
    DeepL, etc.) without refactoring the calling code.

    Usage:
        translator = TranslationEngine(target_lang="Tamil")
        text = translator.translate("HELLO")  # → "வணக்கம்"

    To add a new language:
        1. Add entry to LANGUAGE_CONFIG
        2. Add translations to TRANSLATION_DICT
        3. (Optional) Implement API-based fallback in _api_translate()
    """

    def __init__(self, target_lang="English"):
        self.target_lang = target_lang

    @property
    def is_active(self):
        return self.target_lang != "English"

    def translate(self, text: str) -> str:
        if not self.is_active or not text:
            return text

        if self.target_lang == "Tamil":
            return translate_to_tamil(text)

        lang_dict = TRANSLATION_DICT.get(self.target_lang, {})
        upper_text = text.upper().strip()

        # 1. Try full-phrase match
        if upper_text in lang_dict:
            return lang_dict[upper_text]

        # 2. Try word-by-word translation
        words = upper_text.split()
        translated_words = []
        for word in words:
            if word in lang_dict:
                translated_words.append(lang_dict[word])
            else:
                translated_words.append(word)

        result = " ".join(translated_words)

        # 3. If no dictionary match at all, try API (fallback)
        if result == upper_text and upper_text:
            result = self._api_translate(text)

        return result

    def _api_translate(self, text: str) -> str:
        """
        Translates text using Google Translate via deep-translator.
        Falls back to original text if offline or if an error occurs.
        """
        try:
            from deep_translator import GoogleTranslator
            target_code = LANGUAGE_CONFIG.get(self.target_lang, {}).get("code", "en")
            return GoogleTranslator(source='en', target=target_code).translate(text)
        except Exception as e:
            print(f"[WARN] Translation failed (offline or API error): {e}")
            return text



